    public void query_2() {

        datamodel.getDataVector().clear();
        listSignUp.clear();

        text = inputMaLop.getText();
        if (text.equals("")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập mã lớp", "Error", JOptionPane.INFORMATION_MESSAGE);
        } else {
            ArrayList<CreditStudent> ar = new ArrayList();
            ar = DBAction.viewTable(text);
            ArrayList<Subject> subj;
            if (ar != null) {
                for (int i = 0; i < ar.size(); i++) {
                    subj = new ArrayList();
                    subj.add(DBAction.selectSubjectAdm(text, Integer.toString(ar.get(i).getIdStu())));
                    for (int j = 0; j < subj.size(); j++) {
                        datamodel.addRow(loadRowData(subj.get(j).getMidPoint(), subj.get(j).getEndPoint(),
                                 subj.get(j).getAverPoint(), ar.get(i).getIdStu(),
                                subj.get(j).getCl().getIdClass(), subj.get(j).getType()));
                    }
                }
                jTable1.setModel(datamodel);
                TableClick();

            } else {
                JOptionPane.showMessageDialog(null, "Lớp không tồn tại", "Error", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }

