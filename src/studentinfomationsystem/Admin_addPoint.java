    private double getDiemTB(double diemqt, double diemck) {
        double a = (diemqt * this.hesogiuaky + diemck * this.hesocuoiky) * 1000;
        double b = Math.round(a);
        return b / 1000;
    }

    private void xeploai(double diemTB) {
        if (diemTB < 4) {
            loai = "F";
        } else if (4 <= diemTB && diemTB < 5.5) {
            loai = "D";
        } else if (5.5 <= diemTB && diemTB < 7) {
            loai = "C";
        } else if (7 <= diemTB && diemTB < 8.5) {
            loai = "B";
        } else {
            loai = "A";
        }
    }

